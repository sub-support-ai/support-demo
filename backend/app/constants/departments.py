"""Источник истины для списка отделов системы поддержки.

Отделы используются в нескольких местах, и расхождение между ними — частый
источник багов:
  - Pydantic-схема разрешит "security", БД-CHECK откажет → 500 при INSERT;
  - AI-классификатор вернёт "facilities", роутер свалит в "IT" по умолчанию;
  - фронт нарисует селектор без новых отделов → пользователь не выберет.

Чтобы такого не было, всё, что зависит от множества отделов, импортит
константы и типы отсюда. Добавление нового отдела = ОДНО место (этот файл)
+ миграция CHECK + frontend-типы.

Почему 7 отделов:
  - IT / HR / finance — базовая троица в MVP, покрывает 80% обращений
    в типичной компании.
  - procurement — закупки (товары, услуги, поставщики). Часто конфликтует
    с finance, потому что оба касаются денег, но procurement — это
    «найти/выбрать/купить», а finance — «оплатить/учесть».
  - security — ИБ-инциденты, доступы к ИБ-системам, расследования.
    Намеренно отделено от IT: SOC живёт по отдельному регламенту,
    время реакции другое (15 минут vs 4 часа), эскалации тоже разные.
  - facilities — АХО / эксплуатация: мебель, освещение, кондиционеры,
    пропуска в здание. На стыке с IT (физическое оборудование), но обычно
    это разные команды.
  - documents — документооборот: договоры, доверенности, нотариальные
    копии, архив. Часто пересекается с finance (счета) и HR (трудовые
    договоры), но управление архивом и бизнес-процессом подписания —
    отдельный домен.
"""

from __future__ import annotations

from typing import Literal

# Кортеж — для runtime-проверок (in / set conversion). Order matters:
# UI отображает в этом порядке (IT первым — самый частый отдел).
DEPARTMENTS: tuple[str, ...] = (
    "IT",
    "HR",
    "finance",
    "procurement",
    "security",
    "facilities",
    "documents",
)

# Set — для быстрых проверок принадлежности (in O(1)).
DEPARTMENTS_SET: frozenset[str] = frozenset(DEPARTMENTS)

# Literal — для Pydantic-схем и type-checking. Дублирует tuple,
# но иначе никак: Literal требует литеральных строк во время компиляции,
# а Literal[*DEPARTMENTS] не сработает.
DepartmentLiteral = Literal[
    "IT",
    "HR",
    "finance",
    "procurement",
    "security",
    "facilities",
    "documents",
]


# Дисплейные имена для UI и email-уведомлений. Хранится здесь, чтобы при
# добавлении отдела не забыть добавить и в перевод.
DEPARTMENT_DISPLAY_NAMES: dict[str, str] = {
    "IT": "ИТ",
    "HR": "Кадры",
    "finance": "Финансы",
    "procurement": "Закупки",
    "security": "Безопасность",
    "facilities": "АХО",
    "documents": "Документооборот",
}


def display_name(department: str) -> str:
    """Возвращает читаемое имя отдела, либо сам код, если перевода нет."""
    return DEPARTMENT_DISPLAY_NAMES.get(department, department)
